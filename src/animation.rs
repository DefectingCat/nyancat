// 颜色映射
pub fn get_color_code(c: char) -> u8 {
    match c {
        '@' => 1, // 红色
        '$' => 3, // 黄色
        '&' => 2, // 绿色
        '+' => 6, // 青色
        '#' => 4, // 蓝色
        '*' => 5, // 紫色
        _ => 0,   // 黑色
    }
}

// 渲染帧到终端
pub fn render_frame(
    frame: &[&str],
    terminal_width: u16,
    terminal_height: u16,
    no_clear: bool,
) -> io::Result<()> {
    let mut stdout = io::stdout();

    if !no_clear {
        execute!(
            stdout,
            crossterm::terminal::Clear(crossterm::terminal::ClearType::All)
        )?;
    }
    execute!(stdout, cursor::MoveTo(0, 0))?;

    // 计算裁剪范围
    let term_half_width = (terminal_width / 2) as usize;
    let min_col = (FRAME_WIDTH.saturating_sub(term_half_width)).saturating_div(2);
    let max_col = min_col + term_half_width;
    let min_row = (FRAME_HEIGHT.saturating_sub(terminal_height as usize)).saturating_div(2);
    let max_row = min_row + terminal_height as usize;

    // 渲染帧内容
    for (y, row) in frame.iter().enumerate() {
        if y < min_row || y >= max_row {
            continue;
        }

        let mut line = String::new();
        for (x, c) in row.chars().enumerate() {
            if x < min_col || x >= max_col {
                continue;
            }

            let color = get_color_code(c);
            println!("color: {}", color);
            line.push_str(&format!(
                "\x1B[3{}m  \x1B[0m", // 双宽字符显示
                color
            ));
        }
        println!("{}", line);
    }

    stdout.flush()?;
    Ok(())
}
